function J = jac(~, y, k, K, KlA, ~, ~, Ks)


y1=y(1); y2=y(2); y3=y(3); y4=y(4); y5=y(5); y6=y(6);
k1=k(1); k2=k(2); k3=k(3); k4=k(4);

J =[
      -k3*y4^2-8*k1*y1^3*y2^(1/2)-(k2*y5)/K,  -(k1*y1^4)/y2^(1/2),  k2*y4,   k2*y3-2*k3*y1*y4, -(k2*y1)/K, 0;
      -k3*y4^2-2*k1*y1^3*y2^(1/2), -KlA-(k1*y1^4)/(4*y2^(1/2))-(k4*y6^2)/(4*y2^(1/2)), 0, -2*k3*y1*y4, 0, -k4*y2^(1/2)*y6;
      4*k1*y1^3*y2^(1/2)+(k2*y5)/K, (k1*y1^4)/(2*y2^(1/2)), -k2*y4,  -k2*y3, (k2*y1)/K,  0;
      (k2*y5)/K-2*k3*y4^2, 0, -k2*y4, -k2*y3-4*k3*y1*y4, (k2*y1)/K, 0;
      -(k2*y5)/K, (k4*y6^2)/(2*y2^(1/2)), k2*y4, k2*y3, -(k2*y1)/K, 2*k4*y2^(1/2)*y6;
      Ks*y4, 0, 0, Ks*y1, 0, -1];
 
end
