function [f, df] = buhmann6(k)

maskk0 = k == 0;

f = (1/15) + (19/6)*(k.^2) - (16/3)*(k.^3) + 3*(k.^4) - (16/15)*(k.^5) + (1/6)*(k.^6) + 2*(k.^2).*log(k);
f(maskk0) = (1/15) + (19/6)*(k(maskk0).^2) - (16/3)*(k(maskk0).^3) + 3*(k(maskk0).^4) - (16/15)*(k(maskk0).^5) + (1/6)*(k(maskk0).^6);


df = (25/3)*(k) - (16)*(k.^2) + 12*(k.^3) - (16/3)*(k.^4) + k.^5 + 4*k.*log(k);
df(maskk0) = (25/3)*(k(maskk0)) - (16)*(k(maskk0).^2) + 12*(k(maskk0).^3) - (16/3)*(k(maskk0).^4) + k(maskk0).^5;

end
