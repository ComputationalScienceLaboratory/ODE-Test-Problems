function dy = f(t, y, ~, R, Ub, UF, alpha, beta)

Ue = 0.1*sin(200*pi*t);  

g23 = beta*(exp((y(2, :) - y(3, :))/UF) - 1);
g56 = beta*(exp((y(5, :) - y(6, :))/UF) - 1);

dy = [(y(1, :) - Ue)/R(1); ...
    -Ub/R(3) + y(2, :)*(1/R(3) + 1/R(2)) - (alpha - 1)*g23; ...
    -g23 + y(3, :)/R(4); ...
    (-Ub + y(4, :))/R(5) + alpha*g23; ...
    -Ub/R(7) + y(5, :)*(1/R(6) + 1/R(7)) - (alpha - 1)*g56; ...
    -g56 + y(6, :)/R(8); ...
    (-Ub + y(7, :))/R(9) + alpha*g56; ...
    y(8, :)/R(10)];

end
